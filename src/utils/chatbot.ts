interface ChatbotResponse {
  text: string;
  suggestedDoctorId?: string; // We can suggest a doctor based on specialty
}

export const getSmartChatbotResponse = (message: string): ChatbotResponse => {
  const text = message.toLowerCase().trim();

  // 1. Heart / Cardiology Symptoms
  if (text.includes('قلب') || text.includes('سینه') || text.includes('تپش') || text.includes('فشار خون')) {
    return {
      text: `درد یا تپش در قفسه سینه مسئله بسیار حیاتی است.
      
🩺 توصیه موقت:
۱. از هرگونه فعالیت بدنی شدید یا استرس‌زا فوراً دوری کنید.
۲. در صورتی که احساس تنگی نفس، انتشار درد به دست چپ، فک یا تعریق سرد دارید، بدون معطلی با اورژانس (۱۱۵) تماس بگیرید.
۳. در صورت خفیف بودن علائم، توصیه می‌شود سریعاً یک نوبت ویزیت با دکتر سارا علوی یا دکتر سامان پناهی در بخش قلب و عروق رزرو نمایید.`,
      suggestedDoctorId: 'doc-1'
    };
  }

  // 2. Skin / Dermatology Symptoms
  if (text.includes('پوست') || text.includes('مو') || text.includes('ریزش') || text.includes('جوش') || text.includes('لک') || text.includes('تزریق') || text.includes('فیلر')) {
    return {
      text: `مراقبت از پوست و مو نیازمند تشخیص حضوری نوع پوست است.
      
🩺 توصیه موقت:
۱. از مصرف خودسرانه پمادهای کورتون‌دار یا محصولات شیمیایی قوی بدون نسخه پرهیز کنید.
۲. ریزش مو می‌تواند ناشی از کمبود ویتامین، نوسانات هورمونی یا استرس باشد.
۳. جوانسازی و تزریق ژل/بوتاکس حتما باید در کلینیک‌های استاندارد پزشکی انجام شود.
۴. پیشنهاد می‌شود جهت مشاوره تخصصی، نوبتی نزد دکتر امیرحسین کریمی یا دکتر نیلوفر بهرامی رزرو فرمایید.`,
      suggestedDoctorId: 'doc-2'
    };
  }

  // 3. Brain / Neurology Symptoms
  if (text.includes('سردرد') || text.includes('میگرن') || text.includes('کمر') || text.includes('دیسک') || text.includes('اعصاب') || text.includes('تشنج') || text.includes('صرع')) {
    return {
      text: `اختلالات عصبی و سردردهای شدید نیاز به بررسی ریشه‌ای دارند.
      
🩺 توصیه موقت:
۱. در سردردهای میگرنی، استراحت در اتاق تاریک و بدون صدا و مصرف آب کافی موثر است.
۲. اگر سردرد همراه با تاری دید ناگهانی، ضعف در یک طرف بدن یا تب بالا است، سریعاً به اورژانس مراجعه کنید.
۳. درمان دیسک کمر نیازمند ام‌آر‌آی (MRI) و معاینه بالینی دقیق است.
۴. دکتر یاسمن راد و دکتر بهنام صابری متخصصین مغز و اعصاب دکترینو آماده پذیرش شما هستند.`,
      suggestedDoctorId: 'doc-3'
    };
  }

  // 4. Psychiatry / Mental Health
  if (text.includes('استرس') || text.includes('افسردگی') || text.includes('اضطراب') || text.includes('پانیک') || text.includes('وسواس') || text.includes('خواب') || text.includes('بیخوابی')) {
    return {
      text: `سلامت روان به اندازه سلامت جسم حائز اهمیت است.
      
🩺 توصیه موقت:
۱. در هنگام حملات پانیک یا اضطراب شدید، روی تنفس شکمی عمیق (۴ ثانیه دم، ۴ ثانیه مکث، ۴ ثانیه بازدم) تمرکز کنید.
۲. بهبود بهداشت خواب (عدم استفاده از گوشی قبل خواب، تاریک کردن اتاق) اولین قدم برای درمان بیخوابی است.
۳. گفتگو با متخصص روان‌پزشکی می‌تواند راهکارهای علمی موثری را در اختیارتان بگذارد.
۴. رزرو نوبت با دکتر رضا مهدوی متخصص اعصاب و روان می‌تواند بسیار کمک‌کننده باشد.`,
      suggestedDoctorId: 'doc-4'
    };
  }

  // 5. Pregnancy / Gynecology
  if (text.includes('زنان') || text.includes('بارداری') || text.includes('زایمان') || text.includes('پریود') || text.includes('قاعدگی') || text.includes('نازایی') || text.includes('رحم')) {
    return {
      text: `سلامت بانوان نیازمند پایش‌های دوره‌ای و دقیق است.
      
🩺 توصیه موقت:
۱. در صورت بارداری، مصرف اسید فولیک تحت نظر پزشک الزامی است.
۲. دردهای شدید قاعدگی یا خونریزی‌های غیرعادی نیاز به سونوگرافی و بررسی هورمونی دارند.
۳. چکاپ‌های سالانه پاپ‌اسمیر را جدی بگیرید.
۴. جهت مشاوره و درمان می‌توانید از خانم دکتر المیرا حسینی یا خانم دکتر شقایق رحیمی نوبت رزرو کنید.`,
      suggestedDoctorId: 'doc-5'
    };
  }

  // 6. Pediatrics / Children
  if (text.includes('کودک') || text.includes('بچه') || text.includes('نوزاد') || text.includes('طفل') || text.includes('رشد') || text.includes('واکسن')) {
    return {
      text: `سیستم ایمنی کودکان بسیار حساس است و تشخیص سریع اهمیت بالایی دارد.
      
🩺 توصیه موقت:
۱. از تجویز هرگونه داروی بزرگسالان به کودکان جداً خودداری کنید.
۲. پایش قد و وزن نوزاد در ماه‌های اولیه زندگی باید به صورت ماهانه انجام شود.
۳. تب در کودکان زیر ۳ ماه همواره یک فوریت پزشکی است.
۴. پیشنهاد می‌شود جهت معاینه تخصصی اطفال، نوبتی نزد دکتر آرش پارسا رزرو نمایید.`,
      suggestedDoctorId: 'doc-6'
    };
  }

  // 7. General Symptoms: Fever, Cough, Covid
  if (text.includes('تب') || text.includes('سرفه') || text.includes('سرماخوردگی') || text.includes('گلو') || text.includes('کرونا') || text.includes('آنفولانزا')) {
    return {
      text: `علائم سرماخوردگی یا عفونت‌های ویروسی معمولاً با استراحت بهبود می‌یابند.
      
🩺 توصیه موقت:
۱. مایعات گرم فراوان بنوشید و استراحت کافی داشته باشید.
۲. برای کنترل تب می‌توانید از استامینوفن ساده استفاده کنید.
۳. در صورت بروز تنگی نفس شدید یا تداوم تب بالای ۳ روز، حتماً به پزشک مراجعه کنید.
۴. می‌توانید در بخش پزشکان متخصص، پزشک عمومی یا متخصص عفونی را برای بررسی دقیق‌تر جستجو کنید.`
    };
  }

  // Default Clinical Triage Questions (Smart Fallback)
  return {
    text: `سلام. من دستیار هوشمند درمانی دکترینو هستم. 
    
لطفاً برای راهنمایی دقیق‌تر، به سوالات زیر پاسخ دهید:
۱. علائم اصلی شما چیست و از چه زمانی شروع شده است؟
۲. آیا در حال حاضر داروی خاصی مصرف می‌کنید یا بیماری زمینه‌ای دارید؟
۳. شدت علائم خود را از ۱ تا ۱۰ چقدر ارزیابی می‌کنید؟

(همچنین می‌توانید تخصص مورد نظر خود را در پیام بعدی ذکر کنید تا بهترین پزشک را به شما معرفی کنم.)`
  };
};
